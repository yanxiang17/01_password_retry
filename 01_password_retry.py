# 密碼重試程式

# 設定密碼password = 'a123456'
# 讓使用者可重複輸入密碼 
# 最多輸入3次
# 判斷密碼
# 如果正確  印出'登入成功!'
# 如果不正確  印出'密碼錯誤! 還有__次機會!'
# 如果不正確  沒機會時  印出'沒機會嘗試了, 要鎖帳號啦!'


password = 'a123456'
i = 3 # 儲存剩餘機會
while i > 0:
    i -= 1
    pwd = input('請輸入密碼： ')
    if pwd == password:
        print('登入成功!')
        break
    else:
        if i > 0:
            print('密碼錯誤! 還有',i , '次機會!')
        else:
            print('沒有機會嘗試了，要鎖帳號啦!')
            break
        


